#include "libft.h"

int			ft_first_char_unicode(int c)
{
	return ((c & 0x80) == 0x00
			|| (c & 0xc0) == 0xc0
			|| (c & 0xe0) == 0xe0
			|| (c & 0xf0) == 0xf0
			|| (c & 0xf8) == 0xf8
			|| (c & 0xfc) == 0xfc);
}
